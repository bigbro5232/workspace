// 숫자형 데이터 타입
console.log(50);
console.log(50.12);
console.log(50 + 7);
console.log(50 - 7);
console.log(50 * 7);
console.log(50 / 7);
// 문자열 데이터 타입
console.log("저는 조하영입니다.");
console.log("잘해 봅시다.");
console.log("1000");
console.log(1000);
// + 연산자의 기능
console.log("Java"+"Script");
// 제어문자
console.log("Java\nScript");
console.log("Java\rScript");
console.log("Java\tScript");
console.log("Java\bScript");
// 이스케이프 문자
console.log("\"JavaScript\" is King");
// 논리형
console.log(true);
console.log(false);
// falsy
console.log("" == false);
console.log(0 == false);
console.log(null == null);
console.log(NaN == NaN);
console.log(Number.isNaN(NaN));
console.log(undefined == undefined);